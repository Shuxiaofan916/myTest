package com.company.bean;

public class Task {
    //任务数
    private Integer number;
    //完成状态
    private Boolean state;

    public Integer getNumber() {
        return number;
    }

    public void setNumber(Integer number) {
        this.number = number;
    }

    public Boolean getState() {
        return state;
    }

    public void setState(Boolean state) {
        this.state = state;
    }
}
